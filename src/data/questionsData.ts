// Helper functions
export const getQuestionsByType = (type: QuestionType): Question[] => {
  return mockQuestions.filter(q => q.type === type);
};

export const getQuestionsBySubject = (subjectId: string): Question[] => {
  return mockQuestions.filter(q => q.subjectId === subjectId);
};

export const getQuestionsByParagraph = (paragraphId: string): Question[] => {
  return mockQuestions
    .filter(q => q.paragraphId === paragraphId)
    .sort((a, b) => (a.paragraphOrder || 0) - (b.paragraphOrder || 0));
};

export const getParagraphIds = (): string[] => {
  const paragraphIds = new Set<string>();
  mockQuestions.forEach(q => {
    if (q.paragraphId) paragraphIds.add(q.paragraphId);
  });
  return Array.from(paragraphIds);
};

export const getQuestionsByStatus = (status: QuestionStatus): Question[] => {
  return mockQuestions.filter(q => q.status === status);
};

export const getQuestionsByLanguage = (language: QuestionLanguage): Question[] => {
  return mockQuestions.filter(q => q.language === language);
};

export const getQuestionsByDifficulty = (difficulty: QuestionDifficulty): Question[] => {
  return mockQuestions.filter(q => q.difficulty === difficulty);
};

// Get unique subjects from questions
export const getUniqueSubjects = (): string[] => {
  return Array.from(new Set(mockQuestions.map(q => q.subject)));
};

// Get unique chapters for a subject
export const getChaptersForSubject = (subjectId: string): string[] => {
  return Array.from(new Set(
    mockQuestions.filter(q => q.subjectId === subjectId).map(q => q.chapter)
  ));
};
